const fs = require('fs');
let c = fs.readFileSync('services/worker/src/index.ts', 'utf8');

const target = '  const sweepResult = await sweepFunds(session);';
const idx = c.indexOf(target);
if (idx < 0) { console.log('NOT FOUND'); process.exit(1); }

const feeBlock = [
  '  // \u2500\u2500 A1: Performance fee deduction \u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500',
  '  // Deduct 0.33% of NET session profit (in SOL) before sweeping funds home.',
  '  // Profit = current SOL balance - starting balance. Fee only applies when',
  '  // profit is positive. Transfer goes to the configured collector wallet.',
  '  // If fee collection fails, log and proceed with sweep \u2014 never block fund return.',
  '  let performanceFeeLamports = 0;',
  '  if (performanceFeeConfig.enabled && performanceFeeConfig.collectorWallet) {',
  '    try {',
  '      const keypair = await getKeypair(session.id);',
  '      if (keypair) {',
  '        const sessionPubkey = keypair.publicKey;',
  '        const currentBalance = await rlGetBalance(sessionPubkey);',
  '        const startingBalance = Number(session.funding.startingBalanceAtomic ?? \'0\');',
  '        const profitLamports = currentBalance - startingBalance;',
  '',
  '        if (profitLamports > 0) {',
  '          const feeLamports = Math.floor(profitLamports * performanceFeeConfig.feeBps / 10_000);',
  '          // Only collect if fee is meaningful (> tx fee) and leaves enough for sweep',
  '          if (feeLamports > TX_FEE_LAMPORTS * 2) {',
  '            const collectorPubkey = new PublicKey(performanceFeeConfig.collectorWallet);',
  '            const feeIxs = [',
  '              SystemProgram.transfer({',
  '                fromPubkey: sessionPubkey,',
  '                toPubkey: collectorPubkey,',
  '                lamports: feeLamports,',
  '              }),',
  '            ];',
  '',
  '            const { blockhash, lastValidBlockHeight } = await rlGetLatestBlockhash();',
  '            const feeMsg = new TransactionMessage({',
  '              payerKey: sessionPubkey,',
  '              recentBlockhash: blockhash,',
  '              instructions: feeIxs,',
  '            }).compileToV0Message();',
  '',
  '            const feeTx = new VersionedTransaction(feeMsg);',
  '            feeTx.sign([keypair]);',
  '            const feeSig = await rlSendRawTransaction(feeTx.serialize());',
  '',
  '            try {',
  '              const feeConfirmation = await rlConfirmTransaction({',
  '                signature: feeSig,',
  '                blockhash,',
  '                lastValidBlockHeight,',
  '              });',
  '              if (!feeConfirmation.value.err) {',
  '                performanceFeeLamports = feeLamports;',
  '                log(',
  '                  \'info\',',
  '                  session.id,',
  '                  `performance fee collected: ${feeLamports} lamports (${performanceFeeConfig.feeBps} bps of ${profitLamports} profit) sig=${feeSig}`,',
  '                );',
  '              } else {',
  '                log(\'warn\', session.id, `performance fee tx failed on-chain: ${JSON.stringify(feeConfirmation.value.err)}`);',
  '              }',
  '            } catch (confirmErr) {',
  '              log(\'warn\', session.id, `performance fee confirmation failed: ${String(confirmErr)}`);',
  '            }',
  '          } else {',
  '            log(\'info\', session.id, `performance fee too small to collect: ${feeLamports} lamports from ${profitLamports} profit`);',
  '          }',
  '        } else {',
  '          log(\'info\', session.id, `no net profit (balance=${currentBalance} starting=${startingBalance})`);',
  '        }',
  '      }',
  '    } catch (feeErr) {',
  '      log(\'warn\', session.id, `performance fee collection failed: ${String(feeErr)}`);',
  '    }',
  '  }',
  '',
].join('\r\n');

c = c.replace(target, feeBlock + target);
fs.writeFileSync('services/worker/src/index.ts', c);
console.log('DONE - inserted performance fee block before sweepFunds');
