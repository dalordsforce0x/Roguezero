import "dotenv/config";
import pg from "pg";
const u = new URL(process.env.DATABASE_PRIVATE_URL.trim());
u.searchParams.delete("sslmode");
const pool = new pg.Pool({ connectionString: u.toString(), ssl: { rejectUnauthorized: false } });
const sid = "3951496c-5459-4298-8369-fb873e2ef613";
const r = await pool.query(`select status, funding, service_control->'positionsState' as positions, service_control->'lastSignal' as signal, service_control->'rotationState' as rotation from sessions where id=$1`, [sid]);
console.log(JSON.stringify(r.rows[0], null, 2));
const q = await pool.query(`select status, count(*)::int count from execution_queue where session_id=$1 group by status`, [sid]);
console.log('queue', JSON.stringify(q.rows));
await pool.end();
